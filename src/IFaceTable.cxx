int IFaceTable::FindConstant(const char *name) {
	unsigned lo = 0;
	unsigned hi = IFaceTable::constantCount - 1;
	do {
		int idx = (lo+hi)/2;
		int cmp = strcmp(name, constants[idx].name);
		
		if (cmp > 0) {
			lo = idx + 1;
		} else if (cmp < 0) {
			hi = idx - 1;
		} else {
			return idx;
		}
	} while (lo <= hi);
	
	return -1;
}

